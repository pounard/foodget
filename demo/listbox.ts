import { App, Window } from "../src/app";
import { ActionBar } from "../src/container";
import { Signal } from "../src/core";
import { Image, Label } from "../src/display";
import { Button, CheckBox, ImageButton } from "../src/form";
import { ListBox } from "../src/list";

export function initListBoxDemo(app: App): Window {
    const window = new Window("ListBox example");
    const listbox = new ListBox();

    for (const word of WORDS) {
        listbox.addRow(row => {
            row.addChild(new Label(word));
            row.addChild(new CheckBox(word));

            const image = new Image();
            image.setUri(colorImage(randomColor(), 24, 24));
            const button = new ImageButton();
            button.setImage(image);
            row.addChild(button);
        });
    }

    const actionBar = new ActionBar();
    actionBar.addChild(new Label(`This listbox contains ${WORDS.length} elements`));
    const closeButton = new Button("Close");
    closeButton.connect(Signal.Clicked, () => app.closeCurrent());
    actionBar.addChild(closeButton)

    window.addChild(actionBar);
    window.addChild(listbox);
    app.addChild(window);

    return window;
}

interface Color {
    R: number,
    G: number,
    B: number,
}

function colorImage(color: Color, width: number, height: number): string {
    const canvas = document.createElement("canvas");
    canvas.width = width;
    canvas.height = height;
    const ctx = canvas.getContext("2d");
    if (!ctx) {
        throw "Could not create canvas context";
    }
    ctx.fillStyle = `rgb(${color.R}, ${color.G}, ${color.B})`
    ctx.fillRect(0, 0, 24, 24);

    return canvas.toDataURL();
}

function randomColor(): Color {
    return {
        R: Math.floor(Math.random() * 256),
        G: Math.floor(Math.random() * 256),
        B: Math.floor(Math.random() * 256),
    }
}

